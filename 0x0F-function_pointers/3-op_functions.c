#include "3-calc.h"
/**
 * op_add - addition
 * @a: first int
 * @b: second int
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction
 * @a: first int
 * @b: second int
 * Return: diff of and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: first int
 * @b: second int
 * Return: multiple of a nd b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: first int
 * @b: second int
 * Return: a divided by b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * @a: first int
 * @b: second int
 * Return: modulo of a ,b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
